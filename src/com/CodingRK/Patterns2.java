package com.CodingRK;

public class Patterns2 {

    public static void main(String args[]) {

        int i = 25;
        int j = 1;
        int k = 0;

        for (int a = 1; a <= i; ++a) {
            for (int b = 1; b <= a; b++) {

                System.out.print(j);
                System.out.print(k);

            }
            System.out.println("");
        }
    }
}
